#Http协议与Requests库
#GET 获取URL位置的资源
#HEAD 获取该资源的头部信息
#POST 向URL位置资源附件新的信息
#PUT  向URL位置存贮一个资源，覆盖原有信息
#PATCH 给url位置资源局部更新
#DELETE 删除URL位置的资源
#http协议方法       与Requests库方法           功能一致性
#GET				requests.get()				一致
#HEAD				requests.head()             一致
#POST				requests.post()             一致
#PUT                requests.put()              一致
#PATCH              requests.patch()            一致
#DELETE             requests.delete()           一致
